    private void QuantidadeEnter(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_QuantidadeEnter
        // TODO add your handling code here:
        if (evt.getExtendedKeyCode() == KeyEvent.VK_ENTER) {
            try {
                String precoStr = util.formatarString(jTextFieldPrecoMedio.getText());
                String quantidadeStr = util.formatarString(jTextFieldQuantidade.getText());
                float preco = Float.parseFloat(precoStr);
                float quantidade = Float.parseFloat(quantidadeStr);
                float precoTotal = preco * quantidade;
                jTextFieldPrecoTotal.setText(String.valueOf(precoTotal));
            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_QuantidadeEnter

