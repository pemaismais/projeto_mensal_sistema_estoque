    private void carregarFornecedores() {
        FornecedorController fornecedorController = new FornecedorController();
        ArrayList<String> entradasEIds = fornecedorController.getIdsENomes();

        DefaultComboBoxModel<String> comboBoxModel = (DefaultComboBoxModel<String>) jComboBoxFornecedor.getModel();

        // Adicionando os entradaes no modelo do JComboBox
        for (String entrada : entradasEIds) {
            comboBoxModel.addElement(entrada);
        }
    }

